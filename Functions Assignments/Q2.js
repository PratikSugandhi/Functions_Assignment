//  Create a JavaScript function called generateGreeting that takes a name as an argument and returns a
// personalized greeting message. Use this function to greet three different people.

// Function to generate a personalized greeting
const generateGreeting = (name) => {
    return `Hello, ${name}! Welcome!`;
};

// Greet three different people
console.log(generateGreeting('Alice'));  // Output: Hello, Alice! Welcome!
console.log(generateGreeting('Bob'));    // Output: Hello, Bob! Welcome!
console.log(generateGreeting('Charlie')); // Output: Hello, Charlie! Welcome!
